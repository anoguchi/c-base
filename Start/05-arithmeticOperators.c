#include<stdio.h>

int main()
{
    int a = 100, b = 10;
    float ans = 10 / 3;

    printf("a = %d\t b = %d\n", a, b);
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n", a % b);
    printf("%f\n", ans);

    return 0;
}